// Variables are containers that stores a value
// declaration establishes the variable in the code
// assignment gives the variable a value

let x; // This is an declaration
x = 34; // This is an assignment
console.log(x);

let y = 35; // This line declares and establishes the variable
console.log(y);

console.log(`X = ${x}, Y = ${y}`); // This embeds the variables in the message
console.log(typeof x); // This tells you what type of variable a variable is